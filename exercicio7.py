'''
Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3 números.
'''

alg1 = float(input("Informe um número: "))
alg2 = float(input("Informe o segundo número: "))
alg3 = float(input("Informe o terceiro número: "))

media = (alg1 + alg2 + alg3) / 3

print(" A média aritmética dos 3 números é:", media)